# Dichiarare la variabile ‘pi’ e assegnarle 3.14, poi dichiarare la variabile
# ‘raggio’ e assegnarle il valore 2.0, e infine assegnare alla variabile
# ‘perimetro’ il valore del perimetro del cerchio.

pi = 3.14       # Dichiaro 'pi' e le assegno il valore frazionario 3.14
raggio = 2.0    # Dichiaro 'raggio' e le assegno il valore 2.0

perimetro = 2 * pi * raggio     # Dichiaro 'perimetro' e ci assegno il doppio del valore
                                # della moltiplicazione tra il raggio e pi, come da formula.

print(perimetro)    # Riga opzionale dove stampo il valore del perimetro
