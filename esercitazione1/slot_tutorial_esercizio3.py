# Stampare a video il valore dell’espressione 1+1

# Soluzione: usiamo la funzione print, e al suo interno ci inseriamo l'espressione aritmetica 1+1
# ricordiamoci che non serve mettere print(2), e non vi è nemmeno richiesto; l'interprete infatti
# valuterà autonomamente l'espressione prima di stamparla.
# Attenzione anche a non utilizzare gli apici (singoli o doppi), poiché non dobbiamo stampare la
# stringa "1+1", bensì dobbiamo stampare 2, valutato come la somma 1+1.
print(1+1)
