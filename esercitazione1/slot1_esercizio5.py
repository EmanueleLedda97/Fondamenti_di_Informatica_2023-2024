# Dichiarare la variabile ‘numero_gatti’ e assegnarle il valore 44, dopodiché
# assegnare a una nuova variabile chiamata ‘file_gatti’ il valore della
# divisione intera tra ‘numero_gatti’ e 6, e a una variabile ‘resto_gatti’
# il resto della divisione intera tra ‘numero_gatti’ e 6.

# Commento generale: ricordiamoci che l'operatore // viene usato per ottenere il quoziente della
# divisione intera, mentre l'operatore %, detto operatore modulo, viene usato per ottenere il resto
# della divisione intera.
# esempio:
#   5 // 2 viene valutato come 2 (5 diviso 2 fa 2 con resto 1)
#   5 % 2 viene valutato come 1 (5 diviso 2 fa 2 con resto 1)

numero_gatti = 44               # Dichiaro 'numero_gatti' e vi assegno 44

file_gatti = numero_gatti // 6  # Dichiaro 'file_gatti' e vi assegno il quoziente
                                # della divisione intera tra 'numero_gatti' e 6

resto_gatti = numero_gatti % 6  # Dichiaro 'file_gatti' e vi assegno il resto
                                # della divisione intera tra 'numero_gatti' e 6